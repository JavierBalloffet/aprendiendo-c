#include <stdio.h>

int main() {
    /* 1. Verifico la condición de verdadero o falso del 0 (cero) */
    if (0) {
        printf("El numero 0 (cero) es considerado \"verdadero\" (true)\n");
    } else {
        printf("El numero 0 (cero) es considerado \"falso\" (false)\n");
    }

    /* 2. Verifico la condición de verdadero o falso del 1 (uno) */
    if (1) {
        printf("El numero 1 (uno) es considerado \"verdadero\" (true)\n");        
    } else {
        printf("El numero 1 (uno) es considerado \"falso\" (false)\n");
    }

    /* 3. Verifico la condición de verdadero o falso del 2 (dos) */
    if (2) {
        printf("El numero 2 (dos) es considerado \"verdadero\" (true)\n");
    } else {
        printf("El numero 2 (dos) es considerado \"falso\" (false)\n");
    }

	return 0;
}
